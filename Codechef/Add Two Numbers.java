// Add Two Numbers
// Problem code: FLOW001
// https://www.codechef.com/problems/FLOW001


import java.util.*;
class Main
{
    public static void main(String arg[])
    {
        Scanner s=new Scanner(System.in);
        
        int t=s.nextInt();
        
        while(t!=0)
        {
            
            int a,b;
            a=s.nextInt();
            b=s.nextInt();
            System.out.println(a+b);
            t=t-1;
        }
        
        
    }
} 